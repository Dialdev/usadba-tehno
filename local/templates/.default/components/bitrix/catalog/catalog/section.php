<?function reqursion_excluding($element, $output, $filter) {
	$sections = CIBlockSection::GetList(Array(SORT=>"ASC"), Array("IBLOCK_ID"=>$arParams["IBLOCK_ID"], "SECTION_ID"=>$element), true, Array());
	while($section = $sections->GetNext()) {
		if (!in_array($section["ID"], $filter)) $output[] = $section["ID"];
		$output = reqursion_excluding($section["ID"], $output, $filter);
	}
	return($output);
